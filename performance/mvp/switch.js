// MVP test: switch statement (JS equivalent of switch.tish)
let x = 2;
switch (x) {
  case 1:
    console.log("one");
    break;
  case 2:
    console.log("two");
    break;
  case 3:
    console.log("three");
    break;
}
console.log("done");
let y = 99;
switch (y) {
  case 1:
    console.log("one");
    break;
  default:
    console.log("default");
}
console.log("end");
